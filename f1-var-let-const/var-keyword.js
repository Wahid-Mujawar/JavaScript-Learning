//Intro to variables represented as var

//var can store some information
//The information can be used it later
//We can change the information later


var firstName = "Javascript"; //declare a variable
console.log(firstName); //use a variable

//change value
firstName = "ES6:2015";
console.log(firstName);


//Rules for naming the variables

//cannot start a var with number
//Example
//1firstName (invalid)
//firstName1 (valid)

//we can use $ or _ to start a variable (anyplace of the string)
//Example
//$firstName (valid)
//first$Name (valid)

//_firstName (valid)
//first_Name (valid)

//cannot uses spaces
//first Name (invalid)

//convention start with uppercase or lowercase
//Example
