//trim
let firstName = "JavaScript";
console.log(firstName.length);  //length at initial

firstName = firstName.trim(); //trims the spaces from string
console.log(firstName); //print the trimmed string
console.log(firstName.length); //length of new sting

//toUpperCase
console.log(firstName.toUpperCase());

//toUpperCase
console.log(firstName.toLowerCase());

//slice
firstName = firstName.slice(0,4);
console.log(firstName);